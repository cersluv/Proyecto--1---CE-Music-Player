package com.example;

public class User {
    private String nombre;
    private String contraseña;
    private String correo;
    private String provincia;

    public String getNombre() {
        return nombre;
    }

    public String getContraseña() {
        return contraseña;
    }

    public String getCorreo() {
        return correo;
    }

    public String getProvincia() {
        return provincia;
    }

}
